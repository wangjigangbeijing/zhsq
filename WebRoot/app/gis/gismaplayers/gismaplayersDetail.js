function addOrUpdate()
{
	
	
	$.post(getContextPath()+"/gismaplayersController/addOrUpdate",
	{
		id:curId,
				layerid:$('#layerid').val(),
		mapid:$('#mapid').val(),
		layersource:$('#layersource').val(),
		layerstyle:$('#layerstyle').val(),
		labelfields:$('#labelfields').val(),
		infofields:$('#infofields').val(),
		queryfields:$('#queryfields').val(),
		note:$('#note').val()
	},
	function(result){
		var obj = jQuery.parseJSON(result);  
		if(obj.success)
		{
			jSuccess("数据修改成功!",{
				VerticalPosition : 'center',
				HorizontalPosition : 'center'
			});
			
			gobackPage();
			
			//load();
		}
		else
		{
			jError("数据修改失败!",{
				VerticalPosition : 'center',
				HorizontalPosition : 'center'
			});
		}
	});
}


