function viewDetail(id)
{
	$.get(getContextPath()+"/noticereadController/get?id="+id,
		function(result){
			var obj = jQuery.parseJSON(result);  
			if(obj.success)
			{
				$('#modalDetail').show();
				
								$('#noticeid').val(obj.noticeid);
				$('#readerid').val(obj.readerid);
				$('#readtime').val(obj.readtime);

					
			}
		});
}

