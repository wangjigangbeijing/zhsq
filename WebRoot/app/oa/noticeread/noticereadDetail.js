function addOrUpdate()
{
	
	
	$.post(getContextPath()+"/noticereadController/addOrUpdate",
	{
		id:curId,
				noticeid:$('#noticeid').val(),
		readerid:$('#readerid').val(),
		readtime:$('#readtime').val()
	},
	function(result){
		var obj = jQuery.parseJSON(result);  
		if(obj.success)
		{
			jSuccess("数据修改成功!",{
				VerticalPosition : 'center',
				HorizontalPosition : 'center'
			});
			
			gobackPage();
			
			//load();
		}
		else
		{
			jError("数据修改失败!",{
				VerticalPosition : 'center',
				HorizontalPosition : 'center'
			});
		}
	});
}


